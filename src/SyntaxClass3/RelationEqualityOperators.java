package SyntaxClass3;

public class RelationEqualityOperators {

	public static void main(String[] args) {
		
		
		int i=15;
		int y=30;
		System.out.println(i==y);
		System.out.println(i!=y);
		System.out.println(i<y);
		
		double a=30.55;
		double b=40.44;
		boolean result=a>b;
		boolean result1=a!=b;
		boolean result2=a==b;
		System.out.println(result);
		System.out.println(result1);
		System.out.println(result2);

		
		
		
		
	}

}
